class Counter:
    def __init__(self, mertices):
        self.mertices = mertices

    @staticmethod
    def add(self):
        return self.mertices + 1

    @staticmethod
    def minus(self):
        return self.mertices - 1

    @staticmethod
    def clean(self):
        self.mertices = 0

    @staticmethod
    def set_mertices(self, num):
        self.mertices
